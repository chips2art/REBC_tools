function [AA]=AllelicCapSeg_PP_CCF_fit_v3_to_ABSOLUTE_SEGTAB(A)
AA.Chromosome	=A.Chromosome;
AA.Start_Z_bp	=A.Start_bp;
AA.End_Z_bp      =A.End_bp;
AA.n_probes     =A.n_probes;
AA.length       =A.length;  
AA.seg_sigma    =A.sigma_tau;
AA.W            =A.length/3e9;       
AA.total_copy_ratio     =A.tau/2;
AA.modal_total_cn       =round(A.tau);
AA.expected_total_cn    =NaN*A.tau;
AA.total_HZ             =(round(A.tau)==0);
AA.total_amp            =NaN*A.tau;
AA.corrected_total_cn   =NaN*A.tau;
AA.rescaled_total_cn    =NaN*A.tau;
AA.bi_Z_allelic           =NaN*A.tau;
AA.copy_Z_ratio           =A.tau/2;
AA.hscr_Z_a1              =A.cn_minor;
AA.hscr_Z_a2              =A.cn_major;
AA.modal_Z_a1             =A.NA;
AA.modal_Z_a2             =A.NB;
AA.expected_Z_a1          =A.cn_clusterA;
AA.expected_Z_a2          =A.cn_clusterB;
AA.subclonal_Z_a1         =A.NA_CCF<0.9;
AA.subclonal_Z_a2         =A.NB_CCF<0.9;
AA.cancer_Z_cell_Z_frac_Z_a1  =A.NA_CCF;
AA.ccf_Z_ci95_Z_low_Z_a1      =A.NA_CCF-(A.cn_sigma_minor./abs(A.cn_clusterA-1));
AA.ccf_Z_ci95_Z_high_Z_a1     =A.NA_CCF+(A.cn_sigma_minor./abs(A.cn_clusterA-1));
AA.cancer_Z_cell_Z_frac_Z_a2  =A.NB_CCF;
AA.ccf_Z_ci95_Z_low_Z_a2      =A.NB_CCF-(A.cn_sigma_major./abs(A.cn_clusterB-1));
AA.ccf_Z_ci95_Z_high_Z_a2     =A.NB_CCF+(A.cn_sigma_major./abs(A.cn_clusterB-1));
AA.LOH                  =A.NA==0;
AA.HZ                   =(A.NA==0)&(A.NB==0);
AA.SC_HZ                =(A.NA==0)&(A.NB==0)&((A.NA_CCF.*A.NB_CCF<0.9)<0.8);
AA.amp_Z_a1             =NaN*A.tau;
AA.amp_Z_a2             =NaN*A.tau;  
AA.rescaled_Z_cn_Z_a1       =A.cn_clusterA;
AA.rescaled_Z_cn_Z_a2       =A.cn_clusterB;
end
